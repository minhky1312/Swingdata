public class setCBB {


        private String value;
        private String label;

        public setCBB() {
            this.value = value;
            this.label = label;
        }


        public String getValue() {
            return this.value;
        }

        public String getLabel() {
            return this.label;
        }

        @Override
        public String toString() {
            return label;
        }
    }


